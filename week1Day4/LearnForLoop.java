package week1.day4;

public class LearnForLoop {

	public static void main(String[] args) {

		
		for (int i = 0; i <10 ; i++) {
//          initialization ; condition;increment/Decrement
			System.out.println("My Favourite Language is java ");
		
		}
		

		for (int i = 2; i >=0 ; i--) {
//          initialization ; condition;increment/Decrement
			System.out.println(i);
		
		}
		
		
		
	}

}
